<template>
  <div class="login_container">
    <div class="login_box">
      <div class="avatar_box">
        <img src="../assets/logo.png" alt="">
      </div>
      <!--      登录表单区域-->
      <el-form label-width="0px" class="login_form" :model="LoginForm" :rules="loginFormRules" ref="loginFormRef">
        <!--        用户名-->
        <el-form-item prop="username">
          <el-input prefix-icon="el-icon-user" v-model="LoginForm.username"></el-input>
        </el-form-item>
        <!--        密码-->
        <el-form-item prop="password">
          <el-input prefix-icon="el-icon-lock" v-model="LoginForm.password" type="password"></el-input>
        </el-form-item>
        <!--        按钮区域-->
        <el-form-item class="btns">
          <el-button type="primary" @click="login">登录</el-button>
          <el-button type="info" @click="resetLgoinForm">重置</el-button>
        </el-form-item>
      </el-form>
    </div>
  </div>
</template>

<script>
import Message from "element-ui";

export default {
  name: "login",
  data() {
    return {
      //这是登录页面的数据绑定
      LoginForm: {
        username: 'liu',
        password: '123456'
      },
      //这是表单的验证规则
      loginFormRules: {
        //验证用户名是否合法
        username: [
          {required: true, message: '请输入登录名称', trigger: 'blur'},
          {min: 3, max: 10, message: '长度在3到5个字符', trigger: 'blur'}
        ],
        //验证密码是否合法
        password: [
          {required: true, message: '请输入登录密码', trigger: 'blur'},
          {min: 6, max: 10, message: '长度在6-10个字符', trigger: 'blur'}
        ]
      }
    }
  },
  methods: {
    //点击重置按钮，重置登录表单
    resetLgoinForm() {
      console.log(this)
      this.$refs.loginFormRef.resetFields();
    },
    login() {
      this.$refs.loginFormRef.validate(async valid => {
        console.log(valid)
        if (!valid) return;
        this.$http.post('api/login/',
            this.$qs.stringify(this.LoginForm))
            .then((res) => {
              console.log(res.data)
              // 验证返回的是否正确
              if (res.data['code'] === 1){
                Message.Message.success('登录成功')
                // 保存token
                window.sessionStorage.setItem('token', res.data['token'])
                return this.$router.push({path: '/home'})
              }else{
                Message.Message.error(res.data['msg'])
              }
            })
            .catch(() => {
              Message.Message.error('网络错误')
            })
      });
    }
  }
}
</script>

